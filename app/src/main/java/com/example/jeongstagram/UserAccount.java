package com.example.jeongstagram;

public class UserAccount {
    public String name;
    public String email;
    public String uid;
    public String imageUrl;

    public UserAccount(String name, String email, String uid, String imageUrl) {
        this.name = name;
        this.email = email;
        this.uid = uid;
        this.imageUrl = imageUrl;
    }
}
